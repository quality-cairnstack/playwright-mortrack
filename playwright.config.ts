import path from 'path';

// Load environment variables from the local .env file to support local runs without shell exports.
const envFilePath = path.resolve(__dirname, '.env');
dotenv.config({ path: envFilePath });

function requireEnvVar(name: string): string {
  const value = process.env[name];
  if (!value) {
    throw new Error(`Missing required environment variable ${name}. Check ${envFilePath}.`);
  }
  return value;
}

